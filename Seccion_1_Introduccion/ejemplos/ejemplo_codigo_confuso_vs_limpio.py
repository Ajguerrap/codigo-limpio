# Ejemplo de código confuso

def fn(x, y):
    s = x * y
    return s

a = 5
b = 7
c = fn(a, b)
print(c)

# Ejemplo de código limpio

def multiplicar(numero_uno, numero_dos):
    resultado = numero_uno * numero_dos
    return resultado

primer_numero = 5
segundo_numero = 7
producto = multiplicar(primer_numero, segundo_numero)
print(producto)
